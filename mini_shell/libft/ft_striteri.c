#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char	*tmp;

	if (s && f)
	{
		tmp = s;
		while (*s)
		{
			f(s - tmp, s);
			s++;
		}
	}
}
